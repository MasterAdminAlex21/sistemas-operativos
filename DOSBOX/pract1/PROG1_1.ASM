PILASG SEGMENT PARA STACK 'Pila'
DW 32 DUP(16) ;Datos repetidos
PILASG ENDS
DATOSG SEGMENT PARA PUBLIC 'Datos'
FLDA DW 250
FLDB DW 1FH
FLDC DW ?
DATOSG ENDS
CODIGO SEGMENT PARA PUBLIC 'Codigo'
MAIN PROC FAR
ASSUME SS:PILASG, DS:DATOSG, CS:CODIGO
MOV AX, DATOSG
MOV DS, AX ;Se inicializa DS
MOV AX, FLDA
ADD AX, FLDB
MOV FLDC, AX
MOV AX, 4C00H
INT 21H
MAIN ENDP
CODIGO ENDS
END MAIN